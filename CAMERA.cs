using Ion;
///////////////////////////////////////////////////////////////////////////////////////////////////////
using System;
using System.Collections.Generic;
///////////////////////////////////////////////////////////////////////////////////////////////////////
namespace Blaster
{
	public class CAMERA : MONOBEHAVIOUR
	{
		enum MODE { Planet,SpaceStation,Parent };

		// ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■
		//----------------------------------------------------------------------------------------------------
		float3 Position;
		quaternion Rotation;
		//----------------------------------------------------------------------------------------------------
		public float TerrainDistance;
		public float SpaceDistance;
		//----------------------------------------------------------------------------------------------------
		float Distance;
		//----------------------------------------------------------------------------------------------------
		// ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■


		//====================================================================================================
		// ■ START
		//====================================================================================================
		void Start()
		{
			Position = transform.position;
			Rotation = transform.rotation;

			var center = GAMEOBJECT.Find("Center").GetComponent<CENTER>();

			SpaceDistance = math.distance(transform.position, center.transform.position);

			TerrainDistance = SpaceDistance * 0.35f;

			Distance = SpaceDistance;
		}
		//====================================================================================================
		// ■ LATE UPDATE
		//====================================================================================================
		void LateUpdate()
		{
			var center = GAMEOBJECT.Find("Center").GetComponent<CENTER>();

			if (center.State == CENTER.STATE.Space) Distance = math.lerp(Distance, SpaceDistance, 0.1f);
			if (center.State == CENTER.STATE.Terrain) Distance = math.lerp(Distance, TerrainDistance, 0.1f);

			var targetPosition = center.transform.position - center.transform.forward * Distance;
			var targetRotation = quaternion.LookRotation(center.transform.forward, center.transform.up);

			transform.position = targetPosition;
			transform.rotation = targetRotation;

		}
		//====================================================================================================
	}
}
