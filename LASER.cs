namespace Blaster
{
	public class LASER : MONOBEHAVIOUR
	{
		// ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■
		//----------------------------------------------------------------------------------------------------
		float Speed = 100.0f;
		//-----------------------------------------------------------------------------------------------------
		GAMEOBJECT LaserObject;
		//-----------------------------------------------------------------------------------------------------
		// ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■ ■


		//====================================================================================================
		// ■ START
		//====================================================================================================
		void Start()
		{
			GameObject.AudioSource.Play();
		}
		//====================================================================================================
		// ■ UPDATE
		//====================================================================================================
		void Update()
		{
			//Rotate Root 
			var center = new float3(0, 0, 0);
			var axis = math.cross(Transform.Up, Transform.Position - center);
			transform.RotateAround(center, axis, -Speed * TIME.deltaTime);

		}
		//====================================================================================================
		// ■ GUI
		//====================================================================================================
		void OnGUI()
		{
			UI.depth = 0;
		}
		//====================================================================================================
		public void SetLaserObject(GAMEOBJECT laserObject)
		{
			LaserObject = laserObject;
		}
		//-----------------------------------------------------------------------------------------------------
		public void SetSpeed(float speed)
		{
			Speed = Speed + speed;
		}
		//-----------------------------------------------------------------------------------------------------
		void OnCollisionEnter(UnityEngine.Collision other)
		{
			if (other.gameObject.tag == "Alien")
			{
				var alien = other.gameObject.GetComponent<ALIEN>();

				alien.Hit();
				
				//Add Score
				PLAYER.AddScore(10);

//				Destroy(collider.gameObject);

				Destroy(LaserObject);
			}
		}
		//-----------------------------------------------------------------------------------------------------
	}
}
